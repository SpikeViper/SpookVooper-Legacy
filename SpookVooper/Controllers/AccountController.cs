        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Register(RegisterViewModel model, string returnUrl = null)
        {
            ViewData["ReturnUrl"] = returnUrl;
            if (ModelState.IsValid)
            {
                if (_userManager.Users.Any(u => u.UserName.ToLower() == model.Username.ToLower() || 
                                                u.Email.ToLower() == model.Email.ToLower()))
                {
                    ModelState.AddModelError(string.Empty, "That username or email has already been used. Please try a different one.");
                    return View(model);
                }

                var user = new User { Id = "u-" + Guid.NewGuid().ToString(), UserName = model.Username, Email = model.Email, Api_Key = Guid.NewGuid().ToString() };
                var result = await _userManager.CreateAsync(user, model.Password);
                if (result.Succeeded)
                {
                    _logger.LogInformation("User created a new account with password.");

                    var code = await _userManager.GenerateEmailConfirmationTokenAsync(user);
                    var callbackUrl = Url.EmailConfirmationLink(user.Id, code, Request.Scheme);
                    await _emailSender.SendEmailConfirmationAsync(model.Username, callbackUrl);

                    await _signInManager.SignInAsync(user, isPersistent: false);
                    _logger.LogInformation("User created a new account with password.");

                    Console.WriteLine(user.Id);

                    return RedirectToLocal(returnUrl);
                }

                AddErrors(result);
            }

            // If execution got this far, something failed, redisplay the form.
            return View(model);
        }

